    getOptimizedImage: (imageConfig, width = 1200, quality = 85) => {
        if (!imageConfig || !imageConfig.url) return '';
        
        if (imageConfig.url.includes('unsplash.com')) {
            return `${imageConfig.url}&w=${width}&q=${quality}&fit=crop&crop=entropy`;
        }
        
        return imageConfig.url;
    },

