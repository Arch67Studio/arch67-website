// Image optimization and handling
const imageHandler = {
    // Get optimized image URL for different use cases
    getOptimizedImage: (imageConfig, width = 1200, quality = 80) => {
        if (!imageConfig || !imageConfig.url) return '';
        
        // For Unsplash images, we can optimize using their API
        if (imageConfig.url.includes('unsplash.com')) {
            return `${imageConfig.url}&w=${width}&q=${quality}&fit=crop`;
        }
        
        // For local images, return as is (you can add your own optimization here)
        return imageConfig.url;
    },

    // Get appropriate image for different contexts
    getImageForContext: (projectId, context) => {
        const project = projectImages[projectId];
        if (!project) return null;

        switch(context) {
            case 'hero':
                return project.hero;
            case 'thumbnail':
                return project.thumbnail;
            case 'gallery':
                return project.gallery;
            default:
                return project.hero;
        }
    },

    // Preload images for better performance
    preloadImages: (imageUrls) => {
        imageUrls.forEach(url => {
            const img = new Image();
            img.src = url;
        });
    }
};
